class World:
    def __init__(self, data):
        self.tile_list = []

        # load images
        img_dirt = pygame.image.load('img/dirt.png')
        img_grass = pygame.image.load('img/grass.png')

        row_count = 0
        for row in data:
            col_count = 0
            for tile in row:
                if tile == 1:
                    img = pygame.transform.scale(img_dirt, (tile_size, tile_size))
                    rect = img.get_rect()
                    rect.x = col_count * tile_size
                    rect.y = row_count * tile_size
                    tile = (img, rect)
                    self.tile_list.append(tile)
                if tile == 2:
                    img = pygame.transform.scale(img_grass, (tile_size, tile_size))
                    rect = img.get_rect()
                    rect.x = col_count * tile_size
                    rect.y = row_count * tile_size
                    tile = (img, rect)
                    self.tile_list.append(tile)
                col_count += 1
            row_count += 1

    def draw(self):
        for tile in self.tile_list:
            screen.blit(tile[0], tile[1])

class Player:
    def __init__(self, x, y):
        img = pygame.image.load('img/guy1.png')
        self.img = pygame.transform.scale(img, (40, 80))
        self.rect = self.img.get_rect()
        self.rect.x = x
        self.rect.y = y
        self.vel_y = 0
        self.jumped = False

    def update(self):
        dx = 0
        dy = 0

        # handle input
        key = pygame.key.get_pressed()
        if key[pygame.K_SPACE] and not self.jumped:
            self.vel_y = -15
            self.jumped = True
        if not key[pygame.K_SPACE]:
            self.jumped = False
        if key[pygame.K_LEFT]:
            dx -= 5
        if key[pygame.K_RIGHT]:
            dx += 5

        # gravity
        self.vel_y += 1
        if self.vel_y > 10:
            self.vel_y = 10

        # Apply velocity
        dy += self.vel_y

        # collsision
        # TODO: collision

        # update
        self.rect.x += dx
        self.rect.y += dy

        if self.rect.bottom > screen_height:
            self.rect.bottom = screen_height
            dy = 0

    def draw(self):
        screen.blit(self.img, self.rect)


